//Principais tipos de variáveis:

let nomePokemon = "pikachu"
let nivelPokemon = 20
let pontosDeVidaPokemon = 45
let sexoPokemon = 'M'
let selecionavel = true

// Tipo STRING são variáveis que armazenam textos:

nomePokemon = "Texto"
sexoPokemon = 'Letra'

// Tipo NUMBER são variáveis que armazenam números:

nivelPokemon = 1
pontosDeVidaPokemon = 10

// Tipo BOOLEAN são variáveis que armazenam o valores true/false:

selecionavel = false

/*OBS! 
    - {let} cria variáveis que só existem dentro de escopos
    - {var} cria variáveis globais (não recomendado)*/